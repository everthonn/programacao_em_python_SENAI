numero = int(input("digite um numero"))

if numero >0 and numero >10 :
    print("NUMERO POSITIVO E MAIOR QUE 10")
elif numero > 0 and numero == 10 :
    print("NUMERO POSITIVO IGUAL A 10")
elif numero > 0 and numero <10 :
    print("NUMERO POSITIVO E MENOR QUE 10")
else:
    print("numero negativo")